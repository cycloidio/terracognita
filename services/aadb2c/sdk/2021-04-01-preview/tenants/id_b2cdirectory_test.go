var _ resourceids.ResourceId = B2CDirectoryId{}

func TestNewB2CDirectoryID(t *testing.T) {
	id := NewB2CDirectoryID("12345678-1234-9876-4563-123456789012", "example-resource-group", "directoryValue")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroup != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroup'", id.ResourceGroup, "example-resource-group")
	}

	if id.DirectoryName != "directoryValue" {
		t.Fatalf("Expected %q but got %q for Segment 'DirectoryName'", id.DirectoryName, "directoryValue")
	}
}

func TestFormatB2CDirectoryID(t *testing.T) {
	actual := NewB2CDirectoryID("12345678-1234-9876-4563-123456789012", "example-resource-group", "directoryValue").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.AzureActiveDirectory/b2cDirectories/directoryValue"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

